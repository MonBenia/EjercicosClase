// Función que muestra una alerta en la consola
function mostrarAlerta() {
    console.log("¡Hola! Esta es una alerta desde una función en JavaScript.");
}

mostrarAlerta(); // Llamada a la función para mostrar la alerta

function alerta(){ // Función que muestra una alerta en el navegador
    alert("Alerta desde funcion");  
}
alerta(); // Llamada a la función para mostrar la alerta en el navegador

function mostrarError(){ // Función que muestra un error en la consola
    alert("No funciona");
}